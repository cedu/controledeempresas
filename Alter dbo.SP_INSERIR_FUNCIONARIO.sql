ALTER PROCEDURE SP_INSERIR_FUNCIONARIO

	@P_NOME			NVARCHAR(150),
	@P_CPF			NVARCHAR(11),
	@P_MATRICULA	NVARCHAR(8),
	@P_DATAADMISSAO	DATE,
	@P_STATUS		INTEGER,
	@P_IDEMPRESA	UNIQUEIDENTIFIER

AS
BEGIN
	BEGIN TRANSACTION

	INSERT INTO FUNCIONARIO(
		ID,
		NOME,
		CPF,
		MATRICULA,
		DATAADMISSAO,
		STATUS,
		IDEMPRESA)
	VALUES(
		NEWID(),
		@P_NOME,
		@P_CPF,
		@P_MATRICULA,
		@P_DATAADMISSAO,
		@P_STATUS,
		@P_IDEMPRESA
	)

	COMMIT
END
